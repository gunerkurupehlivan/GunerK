      <b>Fotoğraf, Gerçeklik ve İdeoloji</b>
       <p></p>Fotoğraf makinesinin ortaya çıkması ile birlikte, sosyal yaşamın akışında önemli değişiklikler meydana gelmiştir. Fotografik görüntünün yaşamı anlatma,
          sorgulama olanakları, insanların fotoğrafı farklı okumalarına ve yorumlamalarına zemin hazırlamıştır. Özellikle toplumsal değişimlere
           ve dönüşümlere müdahale edebilen fotografik görüntünün gerçekliği ve ideolojik yaklaşımlar arasındaki ilişkiyi değerlendirmek,
            -fotoğrafın günümüzdeki yaygınlığı göz önüne alındığında- gereklilikten öte zorunluluk haline gelmiştir.<br>
            Fotoğrafın bulunuşundan
itibaren sadece gerçeği yansıttığının kabul edilmesi, onu uzun yıllar ayrıcalıklı kılmıştır. Bu kabul, gerçeklik yanında,
 fotoğrafa, tarihi belge olma niteliği de sağlamıştır. Fakat zamanla ideolojik ve manipülatif kullanımlar sonucunda fotografik
  gerçeklik kavramı, genel kabul olmaktan çıkmıştır.<br>
Fotoğraf, tarih boyunca ideolojilerle iç içe yaşamıştır.
 İktidarlar ve çıkar grupları, gerçeklik algısı nedeniyle, fotoğrafı bir algı yönetme ve ikna aracı olarak kullanmıştır.
  Fotoğraf, kullanımının büyük boyutlara ulaştığı günümüzde, kitleleri etkilemede, hâlâ önemli bir araç olarak varlığını
  sürdürmektedir. Fotoğrafın dijital teknoloji ile birleşmesi, gerçeklik sunumunun daha kolay biçimlenmesini sağlamış;
   fakat fotoğrafta ideolojik tercih ve yaklaşımların ortaya konmasıyla ilgili bir dizi tartışmayı da beraberinde getirmiştir.</p>
